#| fig-cap: Simulation to confirm the solution. We let $\beta=\gamma=1$, implying a law of motion of $x_{t+1}=3x_t-x_{t-1}$, and as predicted $x$ declines by a factor of 0.38 each generation.

library(tidyverse)

x <- c(1, 0.381966) # initialize sequence with x_0=2, x_1=0.381966
i <- 2 # counter
while (i < 10) { # only go to 10 generations because we lose accuracy and everything turns negative
   x <- c(x, 3 * x[i] - x[i - 1])
   i <- i + 1
}
ggplot(tibble(x), aes(x = 1:nrow(tibble(x)), y = x)) +
   geom_bar(stat = "identity") +
   xlab("generation")
